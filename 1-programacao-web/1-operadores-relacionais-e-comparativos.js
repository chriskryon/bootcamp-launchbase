/*

>       Maior
>=      Maior ou igual
<       Menor
<=      Menor ou Igual
==      Igual
===     Igual e do mesmo tipo
!=      Diferente de
!===    Diferente, inclsive o tipo

*/

console.log(5 > 1); // true
console.log(5 >= 1); // true
console.log(5 < 1); // false
console.log(5 <= 1); // false
console.log(5 == 1); // false
console.log(5 === 1); // false
console.log(5 != 1); // true
console.log(5 !== '1'); // true