/*

Lógicos:
&&  Conforme a tabela verdade AND
||  Conforme a tabela verdade OR
!   Nega o booleano, not

O que converte para falso: null, NaN, 0, "", undefined
-------------------------------

Aritméticos:
+   Soma
-   Subtração
/   Divisão
*   Multiplicação
%   Resto da divisão

*/

console.log(1 + 1); // 2
console.log(1 - 1); // 0
console.log(1 / 1); // 1
console.log(1 * 1); // 1
console.log(1 % 1); // 0